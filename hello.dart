void main() {
  const int a = 7;
  const int b = 3;

  /// Addition
  int addition = a + b;
  print(addition);

  ///Subtraction
  int subtraction = a - b;
  print(subtraction);

  ///Multiplication
  int multiplication = a * b;
  print(multiplication);

  ///Division
  double division = a / b;
  print(division);

  ///Modulus
  int modulus = a % b;
  print(modulus);
}
